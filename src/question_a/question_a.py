def lowest_number(num_list):
    min_number = min(num_list)
    return min_number

def highest_number(num_list):
    max_number = max(num_list)
    return max_number

def total(num_list):
    total = sum(num_list)
    return total

def average(num_list):
    average_total = sum(num_list)/len(num_list)
    return average_total